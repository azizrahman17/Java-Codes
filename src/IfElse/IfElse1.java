package IfElse;

public class IfElse1 {

	public static void main(String[] args) {
		/*    JAVA 
	/ Control Statement 
		 --> Conditional Statements (Select Statement)
		
	 Structor of If Else ====     if (condition) { Statement Block} Else { Statement Block}*/
	  
	  
	  int age  = 16;
	 
	  	if (age >= 18) {
	  		
	  		System.out.println("Eligible for vote "+age);
	  		
	  	}
	  	else {
	  		
	  		System.out.println("Not Eligible for vote "+age);
	  	}
	  
	  
	  
	}
	
	
}
