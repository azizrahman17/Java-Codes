package String;

public class StringExample {

	public static void main(String[] args) {
		// String is collection of the Characters
		String s = "WelCome";
		
		String s1 = " to Java";
		System.out.println(s.length());    //  length is use to count the charactors
		
		System.out.println(s.concat(s1));  //   CONCAT          gathering two strings
	}

}
