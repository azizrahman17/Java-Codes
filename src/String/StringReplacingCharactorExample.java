package String;

public class StringReplacingCharactorExample {

	public static void main(String[] args) {
		String s = "Wellcome to Java";
		System.out.println(s);
		System.out.println(s.replace('o', 'a')); // is use to Replace the Charactors only
		 
		System.out.println(s.replace("Java", "aziz"));
		
	}

}
